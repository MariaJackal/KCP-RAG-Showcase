from rag_logic import rewrite_with_retry


def rewrite_query(history, new_question, rewriter_model, persona=None):
    """通用型查詢擴展 (Universal Radar Expansion)。"""
    max_history_chars = 2000
    if len(history) > max_history_chars:
        history = "..." + history[-max_history_chars:]

    extra_dimension = ""
    if persona and persona.rewrite_extra_dimensions:
        extra_dimension = "\n\n    " + persona.rewrite_extra_dimensions + "\n"

    prompt = f"""
    <角色設定>
    你是台灣資深的交通警察，專長處理A1、A2、A3交通事故，並精熟台灣的交通法規，負責解答各種交通事故處理及相關法規、程序等問題。

    <核心任務>
    當問題涉及交通事故，請優先套用事故分級與現場處理 SOP，再補齊刑事、行政與內部規範關鍵詞。

    <事故分類基準>
    - A1類：當場死亡或 24 小時內死亡。
    - A2類：受傷或超過 24 小時死亡。
    - A3類：僅車損或財損、無人傷亡。

    <通用法規雷達（必做）>
    1. 刑事責任：是否涉及刑法罪名、肇事逃逸（刑法第185-4條）等。
    2. 行政法規：是否涉及道路交通管理處罰條例、道路交通安全規則、罰鍰吊扣等。
    3. 作業程序（SOP）：現場處置、通報、蒐證、製作筆錄、移送與建檔流程。
    4. 內部規範：是否涉及警察機關內部通報、鑑識或偵查隊到場程序。

    <術語正規化（必做，優先於其他維度）>
    使用者輸入常為台灣口語縮寫，搜尋前必須展開成法規正式用語：

    【車種類】
    - 「大型重機」=「大型重型機車」（排氣量逾 250cc，依道路交通管理處罰條例 §92 比照小型汽車）→ 關鍵字必須包含「大型重型機車 比照小型汽車 第92條」
    - 「重機」/「輕機」/「機車」在法規上同屬「機車」→ 關鍵字包含「機車」即可
    - 「大型重機」≠「重機」：兩者法規上屬不同類別，不可混用
    - 「小黃」=「計程車」
    - 「大貨」=「大貨車」；「聯結」=「聯結車」；「拖車」=「拖車」

    【執法/違規用語】
    - 「紅單」=「舉發違反道路交通管理事件通知單」
    - 「酒測」=「酒精濃度檢測」
    - 「肇逃」=「肇事逃逸」

    【燈光違規類】
    - 燈光違規必須依情境分流，不可一律導向第42條：
      1. 「變換車道 / 切換車道 / 轉彎」+「方向燈 / 未打燈」
         → 關鍵字必須包含「汽車駕駛人轉彎或變換車道 第48條 不依規定使用燈光 第42條」
      2. 「慢車 / 自行車」+「夜間 / 大燈 / 未開燈」
         → 關鍵字必須包含「慢車 夜間行車未開啟燈光 第73條」，不可只搜第42條
      3. 其他汽車燈光使用不當（燈號 / 信號燈 / 警示燈 / 危險警告燈 / 未開大燈）
         → 關鍵字包含「不依規定使用燈光 第42條」

    （如遇其他縮寫，依上下文展開為完整法規用語，並保留口語原詞一併作為搜尋關鍵字）

    <車種法定歸屬（必做）>
    依《道路交通管理處罰條例》第 3 條第 8 款：「車輛：指…汽車（包括機車）、慢車及其他行駛於道路之動力車輛。」
    1. 若問題提到「汽車」或未指定車種 → 搜尋範圍包含道路交通管理處罰條例第 2 章全部條文（§21–§68，含 §42 燈光）。
    2. 若問題提到「機車」→ 機車為廣義汽車之一種，仍適用第 2 章（§42 燈光、§48 方向燈、§55/§56 停車等）。搜尋擴增關鍵字必須保留「汽車」「第 2 章」相關詞，不可排除汽車條文。
    3. 若問題提到「慢車」或「自行車」→ 搜尋範圍為第 3 章（§69–§74），不可搜汽車燈光（§42），應搜慢車燈光（§73「夜間行車未開啟燈光」）。
    4. 若問題提到「大型車」「聯結車」「電動車」→ 視為汽車之一種，依第 2 章搜尋。

    <交通事故 SOP 展開（優先）>
    1. 現場管制與救護：
       - A1/A2 先確認 119 救護通報、救護優先。
       - 事故後方警示、交通錐、警戒與防範二次事故。
    2. 現場勘察與蒐證：
       - 查驗駕照、行照、當事人身分，並實施酒測（必備要件）。
       - 現場測繪、全景與特寫照片、煞車痕與散落物紀錄。
       - 查訪目擊者、調閱或保全行車紀錄器影像。
    3. 現場排除與恢復交通：
       - 蒐證完成後清理現場、拖吊排除、恢復車流。
    4. 文書製作：
       - 交通事故談話紀錄表、調查筆錄、道路交通事故當事人登記聯單。
    5. 建檔審核：
       - 現場圖、照片、筆錄上傳與事故處理小組後續審核。

    <實務注意要件（必納入關鍵詞）>
    - 二次事故防範優先於責任爭執。
    - A3 可移車原則（拍照錄影後移置路邊，避免壅塞受罰）。
    - A1/重傷案件之刑案現場保全、封鎖、通報偵查隊與檢察官相驗。
    - 警方角色為客觀蒐證，不介入民事賠償金額判定。
    - 當事人不得逕行離去，注意肇事逃逸構成要件。
    - 現場草圖簽名前應確認車位與行向記載正確。
{extra_dimension}
    <輸出規則>
    - 只輸出一行搜尋字串，不要解釋。
    - 關鍵字之間用空白分隔。
    - 優先放入：事故分類 + SOP步驟 + 法規名稱 + 要件詞。
    - 寧可完整，不可遺漏。

    <歷史對話>{history}</歷史對話>
    <新問題>{new_question}</新問題>
    <輸出>(直接輸出完整搜尋關鍵字串)
    """

    return rewrite_with_retry(
        model=rewriter_model,
        prompt=prompt,
        fallback_text=new_question,
        retries=3,
        timeout=60,
        sleep_seconds=2,
    )

