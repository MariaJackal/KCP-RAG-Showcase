def get_preset(preset_id: str):
    """依 id 取得 preset，找不到回 None。"""
    return next((p for p in PRESETS if p["id"] == preset_id), None)


def _normalize_question(text: str) -> str:
    """比對用正規化：去頭尾空白、去尾端全/半形問號。"""
    return text.strip().rstrip("？?").strip()


def match_preset_question(question: str):
    """使用者打字的問題若與某個 preset 的 question 相同，回傳該 preset。

    容忍尾端問號有無（「毒駕標準作業程序為何」＝「毒駕標準作業程序為何？」），
    其餘須逐字相同；不同問法走正常 RAG pipeline。找不到回 None。
    """
    normalized = _normalize_question(question)
    if not normalized:
        return None
    return next(
        (p for p in PRESETS if _normalize_question(p["question"]) == normalized),
        None,
    )


def list_presets():
    """回傳前端所需的按鈕清單（僅含 id 與 label，不含答案）。"""
    return [{"id": p["id"], "label": p["label"]} for p in PRESETS]
